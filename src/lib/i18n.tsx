const DICTS: Record<Lang, Dict> = { en, sv };

function detectInitial(): Lang {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(STORAGE_KEY) as Lang | null;
  if (stored === "en" || stored === "sv") return stored;
  const nav = window.navigator?.language?.toLowerCase() ?? "";
  return nav.startsWith("sv") ? "sv" : "en";
}

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nCtx = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    setLangState(detectInitial());
  }, []);

  // Pull profile lang once signed in (overrides local default).
  useEffect(() => {
    let active = true;
    const load = async () => {
      const { data } = await supabase.auth.getUser();
      if (!data.user || !active) return;
      const { data: p } = await supabase
        .from("profiles" as any)
        .select("lang")
        .eq("id", data.user.id)
        .maybeSingle();
      const pl = (p as any)?.lang as Lang | undefined;
      if (pl && active && (pl === "en" || pl === "sv")) {
        setLangState(pl);
        try { localStorage.setItem(STORAGE_KEY, pl); } catch {}
      }
    };
    load();
    const { data: sub } = supabase.auth.onAuthStateChange((e) => {
      if (e === "SIGNED_IN") load();
    });
    return () => { active = false; sub.subscription.unsubscribe(); };
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch {}
    // Best-effort persist to profile + auth metadata
    (async () => {
      const { data } = await supabase.auth.getUser();
      if (!data.user) return;
      await supabase.from("profiles" as any).update({ lang: l }).eq("id", data.user.id).select("id");
      await supabase.auth.updateUser({ data: { lang: l } });
    })();
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const raw = DICTS[lang][key] ?? DICTS.en[key] ?? key;
      if (!vars) return raw;
      return raw.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? String(vars[k]) : m));
    },
    [lang],
  );

  return <I18nCtx.Provider value={{ lang, setLang, t }}>{children}</I18nCtx.Provider>;
}

export function useI18n(): Ctx {
  const ctx = useContext(I18nCtx);
  if (!ctx) return { lang: "en", setLang: () => {}, t: (k) => DICTS.en[k] ?? k };
  return ctx;
}

export function LangToggle({ className = "" }: { className?: string }) {
  const { lang, setLang } = useI18n();
  return (
    <div role="group" aria-label="Language" className={`inline-flex items-center gap-0.5 ${className}`}>
      {(["sv", "en"] as Lang[]).map((l) => (
        <button
          key={l}
          type="button"
          onClick={() => setLang(l)}
          aria-pressed={lang === l}
          className={`px-2.5 py-1 rounded-full text-sm font-extrabold uppercase ${
            lang === l ? "bg-[var(--green-pop)] text-[var(--ink)]" : "text-[var(--ink)] opacity-50"
          }`}
        >
          {l === "sv" ? "SV" : "EN"}
        </button>
      ))}
    </div>
  );
}